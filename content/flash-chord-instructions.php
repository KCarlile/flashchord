    <div class="col-md">
        <h4>How do I use Flash Chord?</h4>
        <ol>
            <li><strong>Set a tempo:</strong> set the speed of chord changes;
                start slow if you're a beginner.</li>
            <li><strong>Set the time signature:</strong> choose the time
                signature for your the metronome.</li>
            <li><strong>Select a key:</strong> restrict the chords to a
                specific key or leave it on <em>Any</em> to challenge yourself
                even more.<br /><span class="small">Note: in the current
                version, specifying a key will override the difficulty
                settings.</span></li>
            <li><strong>Set a difficulty:</strong> pick a level that matches
                your abilities including beginner, intermediate, or advanced.
                If advanced is still too easy for you, you can also add chord
                extensions or hide the upcoming chord for the ultimate
                challenge!</li>
            <li><strong>Click Start:</strong> click <em>Start</em> when you're
                ready to begin.
                <ul>
                    <li><strong>Silent practice:</strong> if you'd like to
                        practice silently with out the audio cues, click
                        <em>Disable metronome</em>.</li>
                </ul>
            </li>
            <li><strong>Click Stop:</strong> click <em>Stop</em> when you're
                done.</li>
        </ol>
    </div>
</div>
